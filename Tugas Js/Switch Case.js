var hari = "Selasa";

switch (hari) {
  case "Senin":
    console.log("Hari Senin");
    break;
  case "Selasa":
    console.log("Hari Selasa");
    break;
  case "Rabu":
    console.log("Hari Rabu");
    break;
  case "kamis":
    console.log("Hari Kamis");
    break;
  case "Jumat":
    console.log("Hari Jumat");
    break;
  default:
    console.log("Hari tidak dikenali");
}
